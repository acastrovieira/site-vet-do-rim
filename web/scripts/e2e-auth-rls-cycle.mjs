import { spawnSync } from 'node:child_process'
import { randomBytes } from 'node:crypto'
import { existsSync, readFileSync, rmSync, unlinkSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'
import { createClient } from '@supabase/supabase-js'
import { loadLocalEnv, requiredEnv } from './lib/env-file.mjs'
import {
  deleteAuthUsersAndVerify,
  runCleanupSteps,
} from './lib/e2e-cleanup-match.mjs'
import { explicitSupabaseTarget } from './lib/supabase-target.mjs'

const localEnv = loadLocalEnv()
const { supabaseUrl } = explicitSupabaseTarget(localEnv, { mutation: true })
const serviceRoleKey = requiredEnv(localEnv, 'SUPABASE_SERVICE_ROLE_KEY')
const anonKey = requiredEnv(localEnv, 'NEXT_PUBLIC_SUPABASE_ANON_KEY')

process.env.NEXT_PUBLIC_SUPABASE_URL = supabaseUrl
process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY = anonKey
process.env.SUPABASE_SERVICE_ROLE_KEY = serviceRoleKey

const supabase = createClient(supabaseUrl, serviceRoleKey, {
  auth: {
    autoRefreshToken: false,
    persistSession: false,
  },
})

const runId = new Date().toISOString().replace(/[-:.TZ]/g, '').slice(0, 14)
const roles = ['admin', 'vet', 'tutor']
const createdUsers = []
const envLocalPath = join(process.cwd(), '.env.local')
const nextBuildPath = join(process.cwd(), '.next')
let envLocalSnapshot

function password() {
  return `VetRim-${randomBytes(9).toString('base64url')}!9`
}

async function createRoleUser(role) {
  const email = `e2e-${role}-${runId}@example.test`
  const userPassword = password()

  const { data, error } = await supabase.auth.admin.createUser({
    email,
    password: userPassword,
    email_confirm: true,
    user_metadata: {
      full_name: `E2E ${role}`,
    },
  })

  if (error || !data.user) {
    throw new Error(`Failed to create ${role} user: ${error?.message || 'no user returned'}`)
  }

  createdUsers.push({ id: data.user.id, email, role })

  const { error: profileError } = await supabase
    .from('profiles')
    .upsert({
      id: data.user.id,
      role,
      full_name: `E2E ${role}`,
    }, { onConflict: 'id' })

  if (profileError) {
    throw new Error(`Failed to set ${role} profile: ${profileError.message}`)
  }

  return { email, password: userPassword }
}

async function cleanup() {
  const users = [...createdUsers].reverse()
  await deleteAuthUsersAndVerify(supabase.auth.admin, users, {
    isExpectedUser: (user) => (
      roles.includes(user.role)
      && user.email === `e2e-${user.role}-${runId}@example.test`
    ),
    onDeleted: (user) => {
      console.log(`Deleted E2E ${user.role} user: ${user.email}`)
    },
  })
}

function writeTemporaryPublicEnv() {
  envLocalSnapshot = existsSync(envLocalPath)
    ? readFileSync(envLocalPath, 'utf8')
    : null

  writeFileSync(
    envLocalPath,
    [
      '# Temporary E2E env generated by scripts/e2e-auth-rls-cycle.mjs',
      `NEXT_PUBLIC_SUPABASE_URL=${supabaseUrl}`,
      `NEXT_PUBLIC_SUPABASE_ANON_KEY=${anonKey}`,
      '',
    ].join('\n'),
    'utf8'
  )
}

function restoreEnvLocal() {
  if (envLocalSnapshot === undefined) return

  if (envLocalSnapshot === null) {
    unlinkSync(envLocalPath)
    return
  }

  writeFileSync(envLocalPath, envLocalSnapshot, 'utf8')
}

function clearNextCache() {
  rmSync(nextBuildPath, { recursive: true, force: true, maxRetries: 5, retryDelay: 500 })
}

let primaryError

try {
  writeTemporaryPublicEnv()
  clearNextCache()

  const credentials = {}
  for (const role of roles) {
    credentials[role] = await createRoleUser(role)
    console.log(`Created E2E ${role} user: ${credentials[role].email}`)
  }

  const env = {
    ...process.env,
    NEXT_PUBLIC_SUPABASE_URL: supabaseUrl,
    NEXT_PUBLIC_SUPABASE_ANON_KEY: anonKey,
    E2E_ADMIN_EMAIL: credentials.admin.email,
    E2E_ADMIN_PASSWORD: credentials.admin.password,
    E2E_VET_EMAIL: credentials.vet.email,
    E2E_VET_PASSWORD: credentials.vet.password,
    E2E_TUTOR_EMAIL: credentials.tutor.email,
    E2E_TUTOR_PASSWORD: credentials.tutor.password,
  }

  const playwrightCli = join(process.cwd(), 'node_modules', '@playwright', 'test', 'cli.js')
  const command = process.execPath
  const args = [playwrightCli, 'test', 'tests/e2e/auth.spec.ts']
  const result = spawnSync(command, args, {
    cwd: process.cwd(),
    env,
    stdio: 'inherit',
  })

  if (result.error) {
    throw result.error
  }

  if (result.status !== 0) {
    throw new Error(`Playwright exited with status ${result.status}`)
  }
} catch (error) {
  primaryError = error
}

await runCleanupSteps([
  ['Auth E2E user cleanup', cleanup],
  ['temporary public environment restoration', restoreEnvLocal],
  ['Next.js cache cleanup', clearNextCache],
], { primaryError })
