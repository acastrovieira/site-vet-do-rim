import { spawnSync } from 'node:child_process'
import { randomBytes } from 'node:crypto'
import { existsSync, readFileSync, rmSync, unlinkSync, writeFileSync } from 'node:fs'
import { join } from 'node:path'
import { createClient } from '@supabase/supabase-js'
import { envValue, loadLocalEnv, requiredEnv } from './lib/env-file.mjs'

const localEnv = loadLocalEnv()
const projectRef = envValue(localEnv, 'SUPABASE_PROJECT_REF').value || 'ycclyzoslirpnnwgzrqx'
const supabaseUrl = envValue(localEnv, 'NEXT_PUBLIC_SUPABASE_URL').value || `https://${projectRef}.supabase.co`
const serviceRoleKey = requiredEnv(localEnv, 'SUPABASE_SERVICE_ROLE_KEY')
const anonKey = requiredEnv(localEnv, 'NEXT_PUBLIC_SUPABASE_ANON_KEY')

process.env.NEXT_PUBLIC_SUPABASE_URL = supabaseUrl
process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY = anonKey
process.env.SUPABASE_SERVICE_ROLE_KEY = serviceRoleKey

const supabase = createClient(supabaseUrl, serviceRoleKey, {
  auth: {
    autoRefreshToken: false,
    persistSession: false,
  },
})

const runId = `labcrud-${new Date().toISOString().replace(/[-:.TZ]/g, '').slice(0, 14)}`
const envLocalPath = join(process.cwd(), '.env.local')
const nextBuildPath = join(process.cwd(), '.next')
let envLocalSnapshot
let createdUser

function password() {
  return `VetRim-${randomBytes(9).toString('base64url')}!9`
}

async function createVetUser() {
  const email = `e2e-vet-${runId}@example.test`
  const userPassword = password()

  const { data, error } = await supabase.auth.admin.createUser({
    email,
    password: userPassword,
    email_confirm: true,
    user_metadata: {
      full_name: `E2E Lab CRUD ${runId}`,
      requested_role: 'vet',
    },
  })

  if (error || !data.user) {
    throw new Error(`Failed to create vet user: ${error?.message || 'no user returned'}`)
  }

  createdUser = { id: data.user.id, email }

  const { error: profileError } = await supabase
    .from('profiles')
    .upsert({
      id: data.user.id,
      role: 'vet',
      full_name: `E2E Lab CRUD ${runId}`,
      ai_quota_limit: 5,
      ai_quota_used: 0,
    }, { onConflict: 'id' })

  if (profileError) {
    throw new Error(`Failed to set vet profile: ${profileError.message}`)
  }

  return { email, password: userPassword }
}

async function cleanupData() {
  const tutorPrefix = `Tutor E2E CRUD ${runId}`
  const petPrefix = `Paciente E2E CRUD ${runId}`

  const { data: pets } = await supabase
    .from('pets')
    .select('id')
    .ilike('nome', `${petPrefix}%`)

  const petIds = pets?.map((pet) => pet.id) ?? []

  if (petIds.length > 0) {
    const { data: laudos } = await supabase
      .from('laudos_pdf')
      .select('id, storage_path')
      .in('pet_id', petIds)

    const storagePaths = laudos?.map((laudo) => laudo.storage_path).filter(Boolean) ?? []
    if (storagePaths.length > 0) {
      const { error } = await supabase.storage.from('laudos').remove(storagePaths)
      if (error) {
        console.error(`Cleanup storage failed; keeping laudos rows as storage references: ${error.message}`)
        return
      }
    }

    const { error: laudosError } = await supabase.from('laudos_pdf').delete().in('pet_id', petIds)
    if (laudosError) console.error(`Cleanup laudos failed: ${laudosError.message}`)

    const { error: petsError } = await supabase.from('pets').delete().in('id', petIds)
    if (petsError) console.error(`Cleanup pets failed: ${petsError.message}`)
  }

  const { error: tutoresError } = await supabase
    .from('tutores')
    .delete()
    .ilike('nome', `${tutorPrefix}%`)
  if (tutoresError) console.error(`Cleanup tutores failed: ${tutoresError.message}`)
}

async function cleanupUser() {
  if (!createdUser) return

  const { error } = await supabase.auth.admin.deleteUser(createdUser.id)
  if (error) {
    console.error(`Cleanup failed for vet (${createdUser.email}): ${error.message}`)
  } else {
    console.log(`Deleted E2E vet user: ${createdUser.email}`)
  }
}

function writeTemporaryPublicEnv() {
  envLocalSnapshot = existsSync(envLocalPath)
    ? readFileSync(envLocalPath, 'utf8')
    : null

  writeFileSync(
    envLocalPath,
    [
      '# Temporary E2E env generated by scripts/e2e-lab-crud-cycle.mjs',
      `NEXT_PUBLIC_SUPABASE_URL=${supabaseUrl}`,
      `NEXT_PUBLIC_SUPABASE_ANON_KEY=${anonKey}`,
      '',
    ].join('\n'),
    'utf8'
  )
}

function restoreEnvLocal() {
  if (envLocalSnapshot === undefined) return

  if (envLocalSnapshot === null) {
    unlinkSync(envLocalPath)
    return
  }

  writeFileSync(envLocalPath, envLocalSnapshot, 'utf8')
}

function clearNextCache() {
  rmSync(nextBuildPath, { recursive: true, force: true, maxRetries: 5, retryDelay: 500 })
}

try {
  writeTemporaryPublicEnv()
  clearNextCache()

  const credentials = await createVetUser()
  console.log(`Created E2E vet user: ${credentials.email}`)

  const env = {
    ...process.env,
    NEXT_PUBLIC_SUPABASE_URL: supabaseUrl,
    NEXT_PUBLIC_SUPABASE_ANON_KEY: anonKey,
    E2E_RUN_ID: runId,
    E2E_VET_EMAIL: credentials.email,
    E2E_VET_PASSWORD: credentials.password,
  }

  const playwrightCli = join(process.cwd(), 'node_modules', '@playwright', 'test', 'cli.js')
  const result = spawnSync(process.execPath, [playwrightCli, 'test', 'tests/e2e/lab-crud.spec.ts'], {
    cwd: process.cwd(),
    env,
    stdio: 'inherit',
  })

  if (result.error) throw result.error
  if (result.status !== 0) throw new Error(`Playwright exited with status ${result.status}`)
} finally {
  await cleanupData()
  await cleanupUser()
  restoreEnvLocal()
  clearNextCache()
}
